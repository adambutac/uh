public class MultiDimentionalArray {

  public static void main(String[] args) {
    Object o = new Object();
    char[] b = new char[1];
    char[] c = new char[1];
    System.out.println("Object o ->" + o);
    System.out.println("Array b ->" + b);
    System.out.println("Array c ->" + c);
    System.out.println("Array b ->" + b);
  }
}
